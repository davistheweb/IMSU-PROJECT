// Method to convert grade letter to point value
public int getPointValue(String grade) {
    switch (grade) {
        case "A":
            return 5;
        case "B":
            return 4;
        case "C":
            return 3;
        case "D":
            return 2;
        case "E":
            return 1;
        case "F":
            return 0;
        default:
            return 0;
    }
}
